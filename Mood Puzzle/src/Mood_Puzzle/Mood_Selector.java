    private void Emotion_ChoserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Emotion_ChoserActionPerformed
        String level;
        if(Emotion_Choser.getSelectedItem() == "Happy" || 
        Emotion_Choser.getSelectedItem() == "Sad" || 
        Emotion_Choser.getSelectedItem() == "Dull" || 
        Emotion_Choser.getSelectedItem() == "Excited") //Happy Sad Dull Excited
        { 
           level = "medium";
           Medium_Level.main(null);
           this.setVisible(false);
        }
        else if(Emotion_Choser.getSelectedItem() == "Frustrated" || 
        Emotion_Choser.getSelectedItem() == "Depressed") //Frustrated Depressed
        {
            level = "easy"; //Easy.setVisible(True);
            System.out.println("Push button working...");
            Easy_Level.main(null);
            this.setVisible(false);
        }
        else if(Emotion_Choser.getSelectedItem() == "Overwhelmed" || 
        Emotion_Choser.getSelectedItem() == "Clound9") //Overwhelmed Clound9
        {
            level = "hard";
            Hard_Level.main(null); 
            this.setVisible(false);
        }  
    }//GEN-LAST:event_Emotion_ChoserActionPerformed

